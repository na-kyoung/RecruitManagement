package com.recruit.util;

// 초기 데이터 설정
public class InitSampleData {
	
	private String[] recruitIdData; // 채용ID
	private String[] companyData; // 회사명
	private String[] fieldData; // 채용분야
	private String[] peopleData; // 채용인원
	private String[] salaryData; // 연봉
	private String[] deadlineData; // 기한
	
	public String[] getRecruitIdData() {
		return recruitIdData;
	}
	public void setRecruitIdData(String[] recruitIdData) {
		this.recruitIdData = recruitIdData;
	}
	public String[] getCompanyData() {
		return companyData;
	}
	public void setCompanyData(String[] companyData) {
		this.companyData = companyData;
	}
	public String[] getFieldData() {
		return fieldData;
	}
	public void setFieldData(String[] fieldData) {
		this.fieldData = fieldData;
	}
	public String[] getPeopleData() {
		return peopleData;
	}
	public void setPeopleData(String[] peopleData) {
		this.peopleData = peopleData;
	}
	public String[] getSalaryData() {
		return salaryData;
	}
	public void setSalaryData(String[] salaryData) {
		this.salaryData = salaryData;
	}
	public String[] getDeadlineData() {
		return deadlineData;
	}
	public void setDeadlineData(String[] deadlineData) {
		this.deadlineData = deadlineData;
	}
	
}
